# ตัวดำเนินการทางคณิตศาสตณ์
# ตัวดำเนินการ operator, ตัวถูกดำเนินการ operand
# A + B ==> (A,B = operand), (+ = operator)


x = 5
y = 10

print("ผลบวก", x + y)
print("ผลลบ", x - y)
print("ผลคูณ", x * y)
print("ผลหาร", x / y)
print("ผลยกกำลัง", x**y)
print("ผลหารปัดเศษ", x // y)
print("ผลหารเอาเศษ", x % y)