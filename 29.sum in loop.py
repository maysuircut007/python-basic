
start, stop = 1, 5

# while
sum = 0
while start <= stop:
    number = int(input("ป้อนตัวเลขที่ต้องการบวก : "))
    sum += number
    start += 1
average = sum / stop
print("ผลบวก = ", sum)
print("ค่าเฉลีย = ", average)


